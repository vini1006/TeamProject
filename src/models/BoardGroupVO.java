package models;

public class BoardGroupVO {
	private int board_group_id;
	private String board_group_title;
	private String board_group_username;
	private String board_group_wtime;
	private String board_group_status;
	
	public int getBoard_group_id() {
		return board_group_id;
	}
	public void setBoard_group_id(int board_group_id) {
		this.board_group_id = board_group_id;
	}
	public String getBoard_group_title() {
		return board_group_title;
	}
	public void setBoard_group_title(String board_group_title) {
		this.board_group_title = board_group_title;
	}
	public String getBoard_group_username() {
		return board_group_username;
	}
	public void setBoard_group_username(String board_group_username) {
		this.board_group_username = board_group_username;
	}
	public String getBoard_group_wtime() {
		return board_group_wtime;
	}
	public void setBoard_group_wtime(String board_group_wtime) {
		this.board_group_wtime = board_group_wtime;
	}
	public String getBoard_group_status() {
		return board_group_status;
	}
	public void setBoard_group_status(String board_group_status) {
		this.board_group_status = board_group_status;
	}
	
	
}
